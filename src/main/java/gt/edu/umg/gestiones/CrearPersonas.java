    /**
     * @param cargarEventos
     * desde un get hacia la url obtiene todos los eventos creados
     */
    private void cargarEventos() {
        try {
            
            URL url = new URL("http://localhost:8080/eventos"); 
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");

            if (conn.getResponseCode() == HttpURLConnection.HTTP_OK) {
                // Usa un lector JSON para analizar la respuesta y llenar la lista de eventos
                Gson gson = new Gson();
                EventoModel[] eventos = gson.fromJson(new InputStreamReader(conn.getInputStream()), EventoModel[].class);

                listaEventos = new ArrayList<>();
                for (EventoModel evento : eventos) {
                    listaEventos.add(evento);
                    ComboBoxMostrarEventos.addItem(evento.getNombreEvento());  // Muestra solo el nombre
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar eventos desde la API.");
        }
    }
    /**
     * @param agregarUsuario()
     * obtiene e id de evento y me diante un post asigna un usuario y su evento
     * 
     */
    private void agregarUsuario() {
        try {
            // Obtiene el evento seleccionado y su ID
            int index = ComboBoxMostrarEventos.getSelectedIndex();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Por favor, selecciona un evento.");
                return;
            }
            int id_evento = listaEventos.get(index).getIdEvento();

           
            String jsonInputString = String.format(
    "{\"nombre\": \"%s\", \"apellido\": \"%s\", \"telefono\": \"%s\", \"correo\": \"%s\", \"eventoModel\": {\"id_evento\": %d}}",
    txtUsuarioNombre.getText(), txtUsuarioApellido.getText(), txtUsuarioTelefono.getText(), txtUsuarioCorreo.getText(), id_evento
);

          
            URL url = new URL("http://localhost:8080/usuario"); 
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("POST");
            conn.setRequestProperty("Content-Type", "application/json; utf-8");
            conn.setDoOutput(true);

           
            try (OutputStream os = conn.getOutputStream()) {
                byte[] input = jsonInputString.getBytes("utf-8");
                os.write(input, 0, input.length);
            }

          
            int responseCode = conn.getResponseCode();
            if (responseCode == HttpURLConnection.HTTP_CREATED || responseCode == HttpURLConnection.HTTP_OK) {
                JOptionPane.showMessageDialog(this, "Usuario agregado con éxito.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al agregar usuario. Código de respuesta: " + responseCode);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al conectar con la API.");
        }
    }
